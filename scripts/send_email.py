    # =====================================================
    # SEND PDF EMAIL
    # =====================================================
    def send_pdf(
        self,
        receiver_email,
        pdf_path,
        student_name,
        doc_type=None
    ):
        """
        doc_type is optional.

        Document type is automatically detected from filenames like:

        Shreyanshi_Yadav_LIET-MLAI-046_certificate.pdf
        Akash_Kumar_Yadav_LIET-MLAI-054_gradecard.pdf
        """

        receiver_email = self.clean_text(
            receiver_email
        )

        student_name = self.clean_text(
            student_name,
            "Student"
        )

        if not receiver_email:
            print(
                f"❌ Receiver email missing for {student_name}"
            )
            return False

        if not os.path.exists(pdf_path):
            print(
                f"❌ File not found: {pdf_path}"
            )
            return False

        if not os.path.isfile(pdf_path):
            print(
                f"❌ Invalid file path: {pdf_path}"
            )
            return False

        if not pdf_path.lower().endswith(".pdf"):
            print(
                f"❌ Only PDF files are supported: {pdf_path}"
            )
            return False

        document_info = self.detect_document_type(
            pdf_path
        )

        attachment_name = self.get_attachment_name(
            pdf_path
        )

        verification_id = self.extract_verification_id(
            pdf_path
        )

        msg = EmailMessage()

        msg["Subject"] = document_info["subject"]
        msg["From"] = self.sender
        msg["To"] = receiver_email

        plain_body = self.build_plain_body(
            student_name=student_name,
            receiver_email=receiver_email,
            document_info=document_info,
            attachment_name=attachment_name,
            verification_id=verification_id
        )

        html_body = self.build_html_body(
            student_name=student_name,
            receiver_email=receiver_email,
            document_info=document_info,
            attachment_name=attachment_name,
            verification_id=verification_id
        )

        msg.set_content(
            plain_body
        )

        msg.add_alternative(
            html_body,
            subtype="html"
        )

        try:
            with open(pdf_path, "rb") as pdf_file:
                pdf_data = pdf_file.read()

            msg.add_attachment(
                pdf_data,
                maintype="application",
                subtype="pdf",
                filename=attachment_name
            )

            ssl_context = ssl.create_default_context()

            with smtplib.SMTP_SSL(
                "smtp.gmail.com",
                465,
                context=ssl_context,
                timeout=45
            ) as smtp:

                smtp.login(
                    self.sender,
                    self.password
                )

                smtp.send_message(
                    msg
                )

            print(
                f"✅ {document_info['document_title']} sent successfully\n"
                f"   Student: {student_name}\n"
                f"   Email: {receiver_email}\n"
                f"   File: {attachment_name}\n"
                f"   Verification ID: {verification_id}"
            )

            return True

        except smtplib.SMTPAuthenticationError:
            print(
                "❌ Gmail authentication failed.\n"
                "Use a valid Gmail App Password, not your normal Gmail password."
            )
            return False

        except smtplib.SMTPRecipientsRefused:
            print(
                f"❌ Receiver email was refused: {receiver_email}"
            )
            return False

        except smtplib.SMTPException as error:
            print(
                f"❌ SMTP error for {student_name}: {error}"
            )
            return False

        except OSError as error:
            print(
                f"❌ File or network error for {student_name}: {error}"
            )
            return False

        except Exception as error:
            print(
                f"❌ Email failed for {student_name}: {error}"
            )
            return False


