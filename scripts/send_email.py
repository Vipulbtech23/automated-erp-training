    # EXTRACT VERIFICATION ID FROM FILE NAME
    # =====================================================
    @staticmethod
    def extract_verification_id(pdf_path):
        filename = os.path.basename(pdf_path)

        match = re.search(
            r"(LIET-MLAI-\d+)",
            filename,
            flags=re.IGNORECASE
        )

        if match:
            return match.group(1).upper()

        return "Not Available"

    # =====================================================
    # SAFE ATTACHMENT NAME
    # =====================================================
